def useampi_arpakuutio():
    
    import random
    noppien_summa = []

    n = int(input("Kuinka monta kertaa haluat heittää noppaa? "))
    for i in range(n):
        print(f"Nopan luku: {random.randint(1,6)}")
        noppien_summa.append
        print(f"Noppien summa: {noppien_summa}")
useampi_arpakuutio()